def addition(a,b):
    print("inside addition function")
    p=a+b
    print("sum is",p)

def subtraction(c,d):
    print("inside subtraction function")
    q=c-d
    print("subtraction value is ",q)

def multiplication(e,f):
    print("inside multiplication function")
    s=e*f
    print("multiplication value is ", s)

def division(x,y):
    print("inside division function")
    t=x/y
    print("division value is ",t)
